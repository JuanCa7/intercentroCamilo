    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $voleto = new voletos();
        $voleto->fk_eventos = $request->fk_evento;
        $voleto->nombre = $request->nombre;
        $voleto->costo = $request->costo;
        $voleto->fk_tipovoleto = $request->fk_tipovoleto;
        $voleto->cantidad = $request->cantidad;
        $voleto->fechacierre = $request->fechaCierre;
        $voleto->save();
        return back()->with('respuesta','Voleto creado con exito');
    }

